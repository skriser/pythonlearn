# 获取数据
def getData():
    reg = re.compile('(\D+)')
    reg2 = re.compile('(\d+)')
    total = getHtml()
    for i in total:
        ip = i.find_all('td')[1].text
        port = i.find_all('td')[2].text
        ip_port = ip + ':' + port
        time1 = i.find_all('td')[8].text
        unit = re.findall(reg, time1)[0]
        days = re.findall(reg2, time1)[0]
        if unit == '天' and int(days) > 7:
            dataList.append(ip_port)

# 调用主函数
def mainIp():
    getData()
    return random.choice(dataList)

